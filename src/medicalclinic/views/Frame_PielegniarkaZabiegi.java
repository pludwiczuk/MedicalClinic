    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        if (JOptionPane.showConfirmDialog(null, "Czy na pewno chcesz usunąć?") == JOptionPane.YES_OPTION) {
            if (ty.usunZabieg(Integer.parseInt(jTextField1.getText()))) {
                JOptionPane.showMessageDialog(null, "Usunięto");
                updateTable();
            } else {
                JOptionPane.showMessageDialog(null, "Nie udało się usunąć");
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        Zabieg z = new Zabieg();
        z.setData(jTextField4.getText());
        z.setRodzajZabiegu(jTextField5.getText());
        z.setOpisZabiegu(jTextArea1.getText());
        if (JOptionPane.showConfirmDialog(null, "Czy na pewno chcesz zaktualizować zabieg?") == JOptionPane.YES_OPTION) {
            if(DBConnection.getInstance().aktualizujZabieg(z, Integer.parseInt(jTextField1.getText()))) {
                JOptionPane.showMessageDialog(null, "Zaktualizowano");
                updateTable();
            }
            else {
                JOptionPane.showMessageDialog(null, "Nie udało się zaktualizować");
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

