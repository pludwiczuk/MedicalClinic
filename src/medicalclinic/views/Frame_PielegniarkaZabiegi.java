    private void jTable1MouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseReleased
        int row = jTable1.getSelectedRow();
        jTextField1.setText(jTable1.getValueAt(row, 0).toString());
        Pielegniarka pie = DBConnection.getInstance().getPielegniarka(Integer.parseInt(jTable1.getValueAt(row, 1).toString()));
        jTextField2.setText(pie.getImie()+" "+pie.getNazwisko());
        idS = Integer.parseInt(jTable1.getValueAt(row, 2).toString());
        jTextField4.setText(jTable1.getValueAt(row, 3).toString());
        jTextField5.setText(jTable1.getValueAt(row, 4).toString());
        jTextArea1.setText(jTable1.getValueAt(row, 5).toString());
    }//GEN-LAST:event_jTable1MouseReleased

