    public int liczbaDni[]
            = {0, 31, 59, 90, 120, 151, 181, 212, 243, 273, 304, 334};
    public boolean przestepny(int rok) {
        return ((rok % 4 == 0 && rok % 100 != 0) || rok % 400 == 0);
    }
    public int dzienTygodnia(int dzien, int miesiac, int rok) {
        int dzienRoku;
        int yy, c, g;
        int wynik;

        dzienRoku = dzien + liczbaDni[miesiac - 1];
        if ((miesiac > 2) && (przestepny(rok) == true)) {
            dzienRoku++;
        }

        yy = (rok - 1) % 100;
        c = (rok - 1) - yy;
        g = yy + (yy / 4);
        wynik = (((((c / 100) % 4) * 5) + g) % 7);
        wynik += dzienRoku - 1;
        wynik %= 7;

        return wynik;
    }

