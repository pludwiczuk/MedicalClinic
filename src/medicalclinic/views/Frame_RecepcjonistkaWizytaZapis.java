    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Wizyta w = new Wizyta();
        Lekarz l = new Lekarz();
        l.setId(Integer.parseInt(jComboBox1.getSelectedItem().toString().split(" ")[0]));
        l.setImie(jComboBox1.getSelectedItem().toString().split(" ")[1]);
        l.setNazwisko(jComboBox1.getSelectedItem().toString().split(" ")[2]);
        l.setSpecjalizacja(jComboBox1.getSelectedItem().toString().split(" ")[3]);
        Lekarz l2 = DBConnection.getInstance().getLekarz(l.getId());
        w.setPacjent(p);
        w.setLekarz(l);
        w.setTypWizyty(jComboBox2.getSelectedItem().toString());
        //w.setNrPokoju(Integer.parseInt(jTextField1.getText()));
        w.setNrPokoju(l2.getNrPokoju());
        w.setData(jComboBox4.getSelectedItem().toString()+"-"+jComboBox5.getSelectedItem().toString()+"-"+jComboBox6.getSelectedItem().toString());
        Harmonogram h = DBConnection.getInstance().getHarmonogram(l.getId());
        w.setGodz(h.getDzienTygodnia()[dzienTygodnia(Integer.parseInt(w.getData().substring(0, 2)), Integer.parseInt(w.getData().substring(3, 5)), Integer.parseInt(w.getData().substring(6, 10)))].getGodzOd());
        w.setCzyPotwierdzona((jComboBox3.getSelectedItem().toString().equals("T") ? true : false));
        w.setCzyOdwolana(false);
        w.setOpis(jTextField5.getText());
        if (r.umowPacjentaNaWizyte(w)) {
            Wizyta w2 = DBConnection.getInstance().getWizytaByPacjent(p.getId(), l.getId(), w.getData());
            JOptionPane.showMessageDialog(null, "Zapisano\n"+"Godzina przyjęcia: "+w.getGodz()+"\nNumerek: "+w2.getNumerek()+"\nGabinet: "+w.getNrPokoju());
        } else {
            JOptionPane.showMessageDialog(null, "Nie udało się zapisać na wizytę");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

