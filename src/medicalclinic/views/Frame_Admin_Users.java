    public void updateTable(String typUzytkownika) {
        DBConnection db = DBConnection.getInstance();
        ResultSet rs = db.getUsersToTable(typUzytkownika);
        if (rs != null) {
            if(typUzytkownika.equals("admin")) jTable1.setModel(DbUtils.resultSetToTableModel(rs));
            else if(typUzytkownika.equals("lekarz")) jTable2.setModel(DbUtils.resultSetToTableModel(rs));
            else if(typUzytkownika.equals("pielegniarka")) jTable3.setModel(DbUtils.resultSetToTableModel(rs));
            else if(typUzytkownika.equals("recepcjonistka")) jTable4.setModel(DbUtils.resultSetToTableModel(rs));
        } else {
            JOptionPane.showMessageDialog(null, "Błąd bazy danych");
        }
    }

    public void updateAllTables() {
        DBConnection db = DBConnection.getInstance();
        ResultSet rs = db.getUsersToTable("admin");
        if (rs != null) {
            jTable1.setModel(DbUtils.resultSetToTableModel(rs));
        } else {
            JOptionPane.showMessageDialog(null, "Błąd bazy danych");
        }
        rs = db.getUsersToTable("lekarz");
        if (rs != null) {
            jTable2.setModel(DbUtils.resultSetToTableModel(rs));
        } else {
            JOptionPane.showMessageDialog(null, "Błąd bazy danych");
        }
        rs = db.getUsersToTable("pielegniarka");
        if (rs != null) {
            jTable3.setModel(DbUtils.resultSetToTableModel(rs));
        } else {
            JOptionPane.showMessageDialog(null, "Błąd bazy danych");
        }
        rs = db.getUsersToTable("recepcjonistka");
        if (rs != null) {
            jTable4.setModel(DbUtils.resultSetToTableModel(rs));
        } else {
            JOptionPane.showMessageDialog(null, "Błąd bazy danych");
        }
    }
    
